public class Assesmentprogram2 {
    public static void main(String[]args){
        int[][]a={{5,4,7},{1,8,3},{9,6,2}};
        int b=a.length;
        int i,j;
        for(i=0;i<b;i++,System.out.println()){
            for(j=0;j<b;j++){
                System.out.print(a[i][j]+" ");
            }
        }
    } 
}